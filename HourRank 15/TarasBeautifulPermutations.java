import java.util.*;

class TarasBeautifulPermutations {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int queries = in.nextInt();
        for (int i = 0; i < queries; i++) {
            int size = in.nextInt();
            int[] array = new int[size];
            for (int j = 0; j < size; j++) {
                array[j] = nextInt();
            }
        }
    }
}
